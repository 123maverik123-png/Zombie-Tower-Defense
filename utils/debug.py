# utils/debug.py
"""Отладочный вывод, управляемый переменной окружения.

Информационные print (загрузка ассетов, инициализация) засоряют консоль
при каждом запуске игры. dprint() молчит по умолчанию и включается через
переменную окружения ZTD_DEBUG=1. Ошибки и предупреждения печатать напрямую
через print() — их видимость важна всегда.
"""
import os

DEBUG = os.environ.get("ZTD_DEBUG", "") not in ("", "0", "false", "False")


def dprint(*args, **kwargs):
    """print, активный только при ZTD_DEBUG=1."""
    if DEBUG:
        print(*args, **kwargs)
