# core/iso.py
"""Изометрическая проекция 2:1 (dimetric).

Вся игровая логика хранит МИРОВЫЕ ПИКСЕЛЬНЫЕ координаты (top-down план):
пути врагов, позиции башен/стен, коллизии, таргетинг. Изометрия — это
исключительно слой ОТРИСОВКИ: мировые пиксели линейно проецируются на
экран в момент рисования, а обратная проекция нужна для клика мышью.

Проекция линейна по мировым пикселям (НЕ по клеткам), поэтому не зависит
от tile_size и одинаково применима к любой точке мира.

Оси:
  +X мира  -> вправо-вниз на экране
  +Y мира  -> влево-вниз на экране
Квадратный тайл размера ts превращается в ромб шириной ts и высотой ts/2.

  world_to_screen(wx, wy) -> (sx, sy)   прямая (для отрисовки)
  screen_to_world(sx, sy) -> (wx, wy)   обратная (для клика мыши)
  depth(wx, wy)           -> число      ключ z-сортировки (что "ближе")

Экранная глубина: чем больше (wx + wy), тем ниже объект на экране и тем
ближе он к камере — рисуется поверх. depth() возвращает именно этот ключ.
"""

# Коэффициенты сжатия проекции. 0.5 / 0.25 дают ромб 2:1
# (ширина ромба тайла = ts, высота = ts/2). Меняются вместе, если
# понадобится другой угол наклона камеры.
ISO_SCALE_X = 0.5
ISO_SCALE_Y = 0.25


def world_to_screen(wx: float, wy: float) -> tuple:
    """Мировые пиксели -> экранные (относительно начала координат мира).

    Смещение камеры (offset) добавляется вызывающим кодом ОТДЕЛЬНО,
    как и в top-down режиме (cx = sx + ox).
    """
    sx = (wx - wy) * ISO_SCALE_X
    sy = (wx + wy) * ISO_SCALE_Y
    return (sx, sy)


def screen_to_world(sx: float, sy: float) -> tuple:
    """Экранные координаты (уже без смещения камеры) -> мировые пиксели.

    Точная обратная к world_to_screen (round-trip совпадает до float).
    """
    wx = (sx / ISO_SCALE_X + sy / ISO_SCALE_Y) * 0.5
    wy = (sy / ISO_SCALE_Y - sx / ISO_SCALE_X) * 0.5
    return (wx, wy)


def depth(wx: float, wy: float) -> float:
    """Ключ z-сортировки: больше -> ближе к камере -> рисовать позже (поверх)."""
    return wx + wy
